#include "./include/libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int,
char*))
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		f(j, &s[i]);
		i++;
		j++;
	}
}
